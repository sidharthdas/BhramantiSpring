package com.sid.dto;

public class HolidayPackage {
	
	private String packageName;
	private String packageDetails;
	private double packagePrice;
	private double packageDiscountPercent;
	public String getPackageName() {
		return packageName;
	}
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}
	public String getPackageDetails() {
		return packageDetails;
	}
	public void setPackageDetails(String packageDetails) {
		this.packageDetails = packageDetails;
	}
	public double getPackagePrice() {
		return packagePrice;
	}
	public void setPackagePrice(double packagePrice) {
		this.packagePrice = packagePrice;
	}
	public double getPackageDiscountPercent() {
		return packageDiscountPercent;
	}
	public void setPackageCiscountPercent(double packageCiscountPercent) {
		this.packageDiscountPercent = packageCiscountPercent;
	}
	
	

}
